function reduceTo1(grid){
	let refls = reflections(grid[0]);
	let stopped = 0;
	for(let r = 0; r < grid.length; r++){
		const row = grid[r];
		for(let i = refls.length-1; i >= 0; i--){
			if(!isReflection(row, refls[i])){
				refls.splice(i, 1);
			}
		}
		if(refls.length <= 1){
			stopped = r;
			break;
		}
	}


	return {refl: refls[0], stopped}

}



function reflections(line){
	let reflections = [];
	for(let i = 1; i < line.length; i++){
		if(isReflection(line, i)){
			reflections.push(i);
		}
	}
	return reflections;
}



function isReflection(line, i){

	if(i == 0 || i == line.length) return false;

	if(i > line.length/2){
		let ref = line.slice(i, line.length);
		ref.reverse();
		return line.slice(2*i - line.length, i).every((el, i) => el == ref[i])
	}
	else{
		let ref = line.slice(0, i);
		ref.reverse();
		return line.slice(i, 2*i).every((el, i) => el == ref[i])
	}

}

