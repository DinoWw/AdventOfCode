const grid = input.split('\n').map(l => l.split(''));

const gridT = transpose(grid);

gridT.forEach(line => {
	let [count, rounds] = [0, 0];

	for(let i = 0; i < line.length; i++){

		if(line[i] == 'O'){
			rounds ++;
			count ++;
		}
		else if(line[i] == '#'){
			line.splice(i-count, count, ...Array.from(Array(count), (el, i) => i < rounds ? 'O' : '.'));
			rounds = 0;
			count = 0;
		}
		else{
			count ++
		}
	}
	line.splice(line.length-count, count, ...Array.from(Array(count), (el, i) => i < rounds ? 'O' : '.'));
})

const output = transpose(gridT).reduce((sum, l, i) => sum + l.reduce((sumL, x) => sumL + (x == 'O' ? grid.length-i : 0), 0), 0);

console.log(output);



function merge(l){
	return l.reduce((acc, x) => acc + x, "");
}



function transpose(matrix){
	return Array.from(matrix[0], (el, i) => Array.from(matrix, (e, j) => matrix[j][i]))
}