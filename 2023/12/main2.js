function combs(row, groups){

	// string to search over
	let s = row;
	let sum = 0;


	if(row.length == 0 && groups.length != 0){
		return 0;
	}

	if(groups.length == 0){
		if(s.indexOf('#') != -1){
			return 0;
		}
		else{ 
			return 1;
		}
	}

	let i = 0;

	let hid = s.indexOf('#');
	// first hashtag index complement
	let hidc = hid == -1 ? -1 : s.length - hid;
	do{
		let pos = match(s, groups[0], i);

		if(pos == -1) break;

		s = s.slice(pos)
		if(hidc != -1 && hidc != s.length - s.indexOf('#')){
			break;
		}
		let a = [s.slice(groups[0] + 1), groups.slice(1)];
		let c;
		if(memo[a] == undefined){
			c = combs(...a, memo);
			memo[a] = c;
		}
		else {
			c = memo[a];
		}

		let qid = s.indexOf("?");
		if(qid != -1 && qid < groups[0]){
			s = s.slice(qid + 1);
		}
		else {
			s = s.slice(groups[0] + 1);
		}
		sum += c;

		i ++;

	}while(true)

	return sum;

	

}




